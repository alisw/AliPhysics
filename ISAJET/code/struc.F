C
      IF(ISTRUC.EQ.3) GO TO 1000
      IF(ISTRUC.EQ.4) GO TO 2000
      IF(ISTRUC.EQ.5) GO TO 3000
      IF(ISTRUC.EQ.6) GO TO 3100
