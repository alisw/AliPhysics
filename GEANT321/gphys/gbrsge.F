#include "geant321/gconsp.inc"
#include "geant321/gcmate.inc"
      DIMENSION C(100),C1(60),C2(40)
      EQUIVALENCE (C(1),C1(1)),(C(61),C2(1))
      SAVE C
#if !defined(CERNLIB_BETHE)
      PARAMETER (AKSI=1.80,ALFA=0.98,VS=0.0001)
