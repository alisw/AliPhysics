  30  F(I,J,K) = LOG( F(I,J,K))*F(I,16,K)/ LOG( F(I,16,K))
  50  FORMAT(7F10.5)
      DO 40 I=1,7
      DO 40 M=1,19
  40  F(I,42,M)=ZEROD
  10  CONTINUE
      IF(X.LT.XMIN) X=XMIN
      IF(X.GT.XMAX) X=XMAX
      QSQ=SCALE**2
      IF(QSQ.LT.QSQMIN) QSQ=QSQMIN
      IF(QSQ.GT.QSQMAX) QSQ=QSQMAX
      XXX=X
      IF(X.LT.PONED) XXX= LOG10(X)+ONEDO
      N=0
  70  N=N+1
      IF(XXX.GT.XX(N+1)) GOTO 70
      A=(XXX-XX(N))/(XX(N+1)-XX(N))
      RM= LOG(QSQ/QSQMIN)/ LOG(TWOD)
      B=RM-AINT(RM)
      M=1+  INT(RM)
      IF(M.GT.18) M=18
      DO 60 I=1,7
      G(I)= (ONED-A)*(ONED-B)*F(I,N,M)+(ONED-A)*B*F(I,N,M+1)
     .    + A*(ONED-B)*F(I,N+1,M)  + A*B*F(I,N+1,M+1)
      IF(N.GE.16) GOTO 65
      IF(I.EQ.7) GOTO 65
          FAC=(ONED-B)*F(I,16,M)+B*F(I,16,M+1)
          G(I)=FAC**(G(I)/FAC)
  65  CONTINUE
      G(I)=G(I)*(ONED-X)**N0(I)
  60  CONTINUE
      UPV=G(1)
      DNV=G(2)
      SEA=G(4)
      STR=G(6)
      CHM=G(5)
      GL =G(3)
      BOT=G(7)
      RETURN
      END
