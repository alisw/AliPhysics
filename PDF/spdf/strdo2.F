         AA=WN+A(2,I)+A(3,I)
   30 F(I)=F(I)*GAMFUN(AA)/((WN+A(2,I)*A(4,I)/AA)*GAMFUN(A(2,I))
     +*GAMFUN(WN+A(3,I)))
