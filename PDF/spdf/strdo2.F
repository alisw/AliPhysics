      S= LOG( LOG( MAX(Q0,SCALE)/QL2)/ LOG(Q0/QL2))
      DO 10 I=1,5
         DO 10 J=1,6
   10 A(J,I)=B2(1,J,I)+S*(B2(2,J,I)+S*B2(3,J,I))
      DO 20 I=1,5
   20 F(I)=A(1,I)*X**A(2,I)*(WN-X)**A(3,I)*(WN+X* (A(4,I)+X*(A(5,I)+X*A
     +(6,I))))
