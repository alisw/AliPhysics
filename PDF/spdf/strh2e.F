         DO 25 I=1,7
  25     F(I,N,M)=F(I,N,M)/(ONED-XX(N))**N0(I)
  20  CONTINUE
      DO 30 J=1,15
      XX(J)= LOG10(XX(J))+ONEDO
      DO 30 I=1,6
      DO 30 K=1,19
