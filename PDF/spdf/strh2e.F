      IF(X.LT.PONED) XXX= LOG10(X)+ONEDO
      N=0
  70  N=N+1
      IF(XXX.GT.XX(N+1)) GOTO 70
      A=(XXX-XX(N))/(XX(N+1)-XX(N))
      RM= LOG(QSQ/QSQMIN)/ LOG(TWOD)
