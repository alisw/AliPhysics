      IF(N.GE.16) GOTO 65
      IF(I.EQ.7) GOTO 65
          FAC=(ONED-B)*F(I,16,M)+B*F(I,16,M+1)
          G(I)=FAC**(G(I)/FAC)
  65  CONTINUE
      G(I)=G(I)*(ONED-X)**N0(I)
  60  CONTINUE
      UPV=G(1)
      DNV=G(2)
      SEA=G(4)
      STR=G(6)
      CHM=G(5)
      GL =G(3)
      BOT=G(7)
