      F(T)=(1-EXP(-T))/T

      ZI=ABS(GIMAG(Z))
      ZR=Z
      IF(ZR .GE. -5 .AND. (R1*(ZR-1))**2+(R2*ZI)**2 .LE. 1) THEN
       G=AC(10)
       H=BC(10)
       DO 1 K = 9,1,-1
       G=G*Z+AC(K)
    1  H=H*Z+BC(K)
       H=((1+G*Z)/(1+H*Z))*Z
      ELSEIF(ZI .GE. 12 .OR.
     1       ZR .GT. -12 .AND. (R3*(ZR+12))**2+(R4*ZI)**2 .GE. 1) THEN
       G=Z+CC(1)
