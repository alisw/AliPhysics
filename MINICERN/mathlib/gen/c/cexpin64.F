        H=H+S16
        IF(BB .NE. Z) GO TO 11
       ELSE
        BB=C1
        IF(1+CONST*ABS(C2) .NE. 1) GO TO 12
        H=0
        CALL MTLPRT(NAME,'C338.1','TOO HIGH ACCURACY REQUIRED')
#if !defined(CERNLIB_DOUBLE)
        CEXPIN=0.
#endif
#if defined(CERNLIB_DOUBLE)
